# To what value does the following expression evaluate?
'12' < '9'

# This expression evaluates to `True` because the operands are strings,
# not integers. Strings are compared character-by-character from left to 
# right when comparing string values.

# So it first compares `'1'` with `'9'`. Python determines that 
# `'1' < '9'` is `True` so that `'12'` must be less than `'9'`.